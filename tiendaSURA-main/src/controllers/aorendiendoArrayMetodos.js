let fila = document.getElementById("fila")


//Recorriendo el arreglo 

productos.forEach(function (producto){
    //creando un div desde JS
    let columna = document.createElement("div")
    columna.classList.add("col")


    //creando una targeta
    let tarjeta = document.createElement("div")
    tarjeta.classList.add("card", "shadow", "text-center", "h-100", "p-5")

    //creando una imagen
    let imagen = document.createElement("img")
    imagen.classList.add("img-fluid", "w-75")
    imagen.src = producto.fotos[0]
    //detectando evento de mouse 
    imagen.addEventListener("mouseover",function(){
        imagen.src = producto.fotos[1]

    })
    imagen.addEventListener("mouseleave", function(){
        imagen.src = producto.fotos[0]
    })

     //creando un titulo
     let titulo = document.createElement("h6")
     titulo.classList.add("fw-bold")
     titulo.textContent = producto.nombre

    
     //creando los precios
     let precio = document.createElement("p")
     precio.classList.add("EM")
     precio.textContent = "$"+producto.precio

     //creando popularidad
     let popularidad = document.createElement("p")
     popularidad.classList.add("text-warning", "pt-5")
     popularidad.textContent ="Popularidad " + producto.popularidad

    //creando la descripcion
    let desc = document.createElement("p")
    desc.classList.add("text-black-50", "d-none")
    desc.textContent = producto.descripcion


    //Definiendo padres e hijos
    tarjeta.appendChild(imagen)
    columna.appendChild(tarjeta)
    fila.appendChild(columna)
    tarjeta.appendChild(titulo)
    titulo.appendChild(precio)
    precio.appendChild(popularidad)
    popularidad.appendChild(desc)
   
    
    


})


